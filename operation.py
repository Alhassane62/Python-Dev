x = int(input("Entrez un Entier : "))
y = float(input("Entrez un nombre : "))

# Addition
som = x + y
print(f"la somme de {x} et {y} est : {som}\n")

# Soustraction
diff = x - y
print(f"la différence de {x} et {y} est : {diff}\n")

# Multiplication
prod = x * y
print(f"la produit de {x} et {y} est : {prod}\n")

#Division
div = x /y 
print(f"la division de {x} et {y} est : {div}\n")

# Modulo
mod = x % y
print(f"le modulo de {x} et {y} est : {mod}")